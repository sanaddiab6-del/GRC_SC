class AiAssetSuggestionService:
    def __init__(self, *, base_dir: str | None = None, provider=None):
        self.base_dir = base_dir or str(Path(__file__).resolve().parents[1])
        self.provider = provider

    def build_asset_suggestion_draft(self, request, request_data: dict[str, Any]) -> dict[str, Any]:
        normalized = _normalize_request_data(request_data)
        context = _resolve_context(request.user, normalized)
        provider = self.provider if self.provider is not None else get_asset_suggestion_provider()

        if provider is None:
            draft = build_asset_suggestion_fallback_draft(normalized, context)
            _annotate_provider_mode(
                draft,
                provider_mode="provider_not_configured_fallback",
                parser_note="provider_mode: provider_not_configured_fallback",
                warning={
                    "code": "provider_not_configured_fallback",
                    "message": "No AI provider is configured; the advisory asset suggestion draft was generated by the deterministic fallback parser.",
                    "affected_fields": ["source_summary", "warnings"],
                    "needs_review": True,
                },
            )
        else:
            try:
                draft = provider.build_asset_suggestion_draft(normalized)
                _annotate_provider_mode(
                    draft,
                    provider_mode="configured_local_provider",
                    parser_note="provider_mode: configured_local_provider",
                )
            except AssetSuggestionProviderError as exc:
                if not exc.allow_fallback:
                    raise
                draft = build_asset_suggestion_fallback_draft(normalized, context)
                _annotate_provider_mode(
                    draft,
                    provider_mode="local_provider_error_fallback",
                    parser_note="provider_mode: local_provider_error_fallback",
                    warning={
                        "code": "local_provider_error_fallback",
                        "message": exc.detail,
                        "affected_fields": ["source_summary", "warnings"],
                        "needs_review": True,
                    },
                )

        _inject_context_summary(draft, normalized, context)
        _apply_duplicate_detection(draft, request.user, context["folder"])
        _refresh_ambiguous_candidates(draft)
        _refresh_overall_confidence(draft)
        _ensure_low_confidence_blocking_question(draft, normalized)

        validate_asset_suggestion_guardrails(draft, normalized, enforce_shape=False)

        serializer = AiAssetSuggestionDraftSerializer(data=draft)
        if not serializer.is_valid():
            raise AssetSuggestionDraftValidationError(
                "ai_asset_suggestion_draft_validation_failed",
                "Generated advisory asset suggestion draft did not pass contract validation.",
                serializer.errors,
            )

        validated_draft = serializer.data
        validate_asset_suggestion_guardrails(validated_draft, normalized)
        return validated_draft


def build_asset_suggestion_draft(
    request,
    request_data: dict[str, Any],
    *,
    base_dir: str | None = None,
    provider=None,
) -> dict[str, Any]:
    service = AiAssetSuggestionService(base_dir=base_dir, provider=provider)
    return service.build_asset_suggestion_draft(request, request_data)


def build_asset_suggestion_fallback_draft(
    request_data: dict[str, Any],
    context: dict[str, Any],
) -> dict[str, Any]:
    scenario_text = request_data.get("scenario_text", "")
    scope_summary = request_data.get("scope_summary", "")
    known_weaknesses = request_data.get("known_weaknesses") or []
    combined_text = "\n".join(filter(None, [scenario_text, scope_summary, *known_weaknesses]))
    locale = request_data.get("user_locale") or DEFAULT_USER_LOCALE

    warnings = [
        {
            "code": "best_effort_duplicate_detection",
            "message": "Duplicate detection is folder-scoped and best-effort because Asset has no direct Perimeter field and many assets do not carry a reference id.",
            "affected_fields": ["duplicate_candidates", "candidate_assets"],
            "needs_review": True,
        }
    ]
    if request_data.get("_user_locale_fallback_applied"):
        warnings.append(
            {
                "code": "user_locale_fallback",
                "message": "Unsupported locale was normalized to 'en' for the advisory asset suggestion draft.",
                "affected_fields": ["user_locale", "source_summary.detected_language"],
                "needs_review": True,
            }
        )

    source_refs = _build_default_source_refs(scenario_text, scope_summary, known_weaknesses)
    candidates = _build_candidate_assets(combined_text, source_refs, context)
    rejected = _build_rejected_candidates(combined_text)
    blocking_questions = _build_blocking_questions(candidates, request_data.get("strict_mode", True))

    return {
        "draft_type": "AiAssetSuggestionDraft",
        "schema_version": SCHEMA_VERSION,
        "source_summary": {
            "detected_language": locale,
            "input_char_count": len(scenario_text),
            "strict_mode_applied": bool(request_data.get("strict_mode", True)),
            "scenario_excerpt": (scenario_text or scope_summary)[:160],
            "provider_mode": "provider_not_configured_fallback",
            "folder_id": context["folder"].id,
            "perimeter_id": context["perimeter"].id,
            "compliance_assessment_id": context["compliance_assessment"].id,
            "risk_assessment_id": getattr(context.get("risk_assessment"), "id", None),
            "parser_notes": [
                "No GRC objects were created or updated.",
                "Fallback advisory asset draft generated from approved case context and scenario heuristics.",
            ],
        },
        "provider_mode": "provider_not_configured_fallback",
        "candidate_assets": candidates,
        "duplicate_candidates": [],
        "ambiguous_candidates": [],
        "rejected_candidates": rejected,
        "warnings": warnings,
        "blocking_questions": blocking_questions,
        "needs_human_review": True,
        "overall_confidence": 0.0,
        "next_allowed_steps": [
            "review_candidate_assets",
            "resolve_duplicates",
            "prepare_asset_commit_dry_run",
        ],
    }


def _normalize_request_data(request_data: dict[str, Any]) -> dict[str, Any]:
    normalized = dict(request_data)
    normalized["user_locale"] = normalized.get("user_locale") or DEFAULT_USER_LOCALE
    normalized["strict_mode"] = bool(normalized.get("strict_mode", True))
    normalized["known_weaknesses"] = list(normalized.get("known_weaknesses") or [])
    return normalized


def _annotate_provider_mode(
    draft: dict[str, Any],
    *,
    provider_mode: str,
    parser_note: str,
    warning: dict[str, Any] | None = None,
) -> None:
    source_summary = draft.setdefault("source_summary", {})
    parser_notes = source_summary.setdefault("parser_notes", [])
    if parser_note not in parser_notes:
        parser_notes.append(parser_note)
    source_summary["provider_mode"] = provider_mode
    draft["provider_mode"] = provider_mode
    if warning is not None:
        warnings = draft.setdefault("warnings", [])
        if not any(item.get("code") == warning["code"] for item in warnings if isinstance(item, dict)):
            warnings.append(warning)


def _resolve_context(user, request_data: dict[str, Any]) -> dict[str, Any]:
    folder = _get_object_or_error(Folder, request_data["folder_id"], "folder_id")
    perimeter = _get_object_or_error(Perimeter, request_data["perimeter_id"], "perimeter_id")
    compliance_assessment = _get_object_or_error(
        ComplianceAssessment,
        request_data["compliance_assessment_id"],
        "compliance_assessment_id",
    )
    risk_assessment = None
    if request_data.get("risk_assessment_id"):
        risk_assessment = _get_object_or_error(
            RiskAssessment,
            request_data["risk_assessment_id"],
            "risk_assessment_id",
        )
    framework = None
    if request_data.get("selected_framework_id"):
        framework = _get_object_or_error(
            Framework,
            request_data["selected_framework_id"],
            "selected_framework_id",
        )

    _assert_visible_to_user(user, folder, "folder_id")
    _assert_visible_to_user(user, perimeter, "perimeter_id")
    _assert_visible_to_user(user, compliance_assessment, "compliance_assessment_id")
    if risk_assessment is not None:
        _assert_visible_to_user(user, risk_assessment, "risk_assessment_id")
    if framework is not None:
        _assert_visible_to_user(user, framework, "selected_framework_id")

    errors = {}
    if perimeter.folder_id and str(perimeter.folder_id) != str(folder.id):
        errors["perimeter_id"] = ["The selected Perimeter does not belong to folder_id."]
    if compliance_assessment.folder_id and str(compliance_assessment.folder_id) != str(folder.id):
        errors["compliance_assessment_id"] = [
            "The selected ComplianceAssessment does not belong to folder_id."
        ]
    if compliance_assessment.perimeter_id and str(compliance_assessment.perimeter_id) != str(perimeter.id):
        errors["compliance_assessment_id"] = [
            "The selected ComplianceAssessment does not belong to perimeter_id."
        ]
    if risk_assessment is not None:
        if risk_assessment.folder_id and str(risk_assessment.folder_id) != str(folder.id):
            errors["risk_assessment_id"] = [
                "The selected RiskAssessment does not belong to folder_id."
            ]
        elif risk_assessment.perimeter_id and str(risk_assessment.perimeter_id) != str(perimeter.id):
            errors["risk_assessment_id"] = [
                "The selected RiskAssessment does not belong to perimeter_id."
            ]
    if errors:
        raise AssetSuggestionDraftValidationError(
            "ai_asset_suggestion_context_invalid",
            "The Step 3A request contains inconsistent case setup references.",
            errors,
        )

    return {
        "folder": folder,
        "perimeter": perimeter,
        "compliance_assessment": compliance_assessment,
        "risk_assessment": risk_assessment,
        "framework": framework,
    }


def _get_object_or_error(model_class, object_id, field_name: str):
    instance = model_class.objects.filter(id=object_id).first()
    if instance is None:
        raise AssetSuggestionDraftValidationError(
            "ai_asset_suggestion_context_not_found",
            f"{field_name} could not be resolved.",
            {field_name: [f"No {model_class.__name__} record was found for the supplied id."]},
        )
    return instance


def _assert_visible_to_user(user, instance, field_name: str) -> None:
    if not getattr(user, "is_authenticated", False):
        return
    try:
        visible_ids = RoleAssignment.get_accessible_object_ids(
            folder=Folder.get_root_folder(),
            user=user,
            object_type=instance.__class__,
        )[0]
    except Exception:
        return
    if str(instance.id) not in {str(item_id) for item_id in visible_ids}:
        raise AssetSuggestionDraftValidationError(
            "ai_asset_suggestion_permission_denied",
            f"{field_name} is not visible to the current user.",
            {field_name: ["You do not have permission to access this object."]},
        )


def _inject_context_summary(
    draft: dict[str, Any],
    request_data: dict[str, Any],
    context: dict[str, Any],
) -> None:
    source_summary = draft.setdefault("source_summary", {})
    source_summary.setdefault("detected_language", request_data.get("user_locale", DEFAULT_USER_LOCALE))
    source_summary.setdefault("input_char_count", len(request_data.get("scenario_text", "")))
    source_summary.setdefault("strict_mode_applied", bool(request_data.get("strict_mode", True)))
    source_summary.setdefault(
        "scenario_excerpt",
        (request_data.get("scenario_text") or request_data.get("scope_summary") or "")[:160],
    )
    source_summary["folder_id"] = context["folder"].id
    source_summary["perimeter_id"] = context["perimeter"].id
    source_summary["compliance_assessment_id"] = context["compliance_assessment"].id
    source_summary["risk_assessment_id"] = getattr(context.get("risk_assessment"), "id", None)
    parser_notes = source_summary.setdefault("parser_notes", [])
    note = "Step 3A remained advisory-only and performed zero database writes."
    if note not in parser_notes:
        parser_notes.append(note)


def _build_default_source_refs(
    scenario_text: str,
    scope_summary: str,
    known_weaknesses: list[str],
) -> dict[str, dict[str, Any]]:
    refs: dict[str, dict[str, Any]] = {}
    if scenario_text:
        refs["scenario"] = _build_source_ref(scenario_text, scenario_text[:160], "T1")
    if scope_summary:
        refs["scope"] = _build_source_ref(scope_summary, scope_summary[:160], "T2")
    if known_weaknesses:
        refs["weaknesses"] = _build_source_ref(
            "\n".join(known_weaknesses),
            known_weaknesses[0][:160],
            "T3",
        )
    return refs


