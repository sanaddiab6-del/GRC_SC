def _build_rejected_candidates(combined_text: str) -> list[dict[str, Any]]:
    lowered = combined_text.lower()
    rejected: list[dict[str, Any]] = []
    if "mfa" in lowered:
        rejected.append(
            {
                "source_label": "No MFA for remote access",
                "recommended_reclassification": "control_candidate",
                "reason": "The phrase describes a missing security capability and should not be forced into asset creation without a concrete service or platform being named.",
                "confidence": 0.83,
            }
        )
    if "pam" in lowered:
        rejected.append(
            {
                "source_label": "No PAM for privileged admin accounts",
                "recommended_reclassification": "control_candidate",
                "reason": "The phrase describes a missing privileged-access management capability and is better handled as a control or process candidate unless a concrete PAM platform is named.",
                "confidence": 0.81,
            }
        )
    return rejected


def _build_blocking_questions(
    candidates: list[dict[str, Any]],
    strict_mode: bool,
) -> list[dict[str, Any]]:
    questions: list[dict[str, Any]] = []
    if any(item.get("ambiguity_flags") for item in candidates):
        questions.append(
            {
                "question_id": "asset-classification-review",
                "question_text": "Which ambiguous candidate assets should remain assets versus be reclassified as evidence, controls, or vulnerabilities?",
                "reason": "Several candidates describe records, documentation, or account surfaces that cannot be safely forced into Asset creation without human review.",
                "affected_fields": ["candidate_assets", "ambiguous_candidates"],
                "severity": "blocking",
            }
        )
    if strict_mode and all(item.get("confidence", 0.0) < 0.75 for item in candidates):
        questions.append(
            {
                "question_id": "low-confidence-asset-scope",
                "question_text": "Which concrete systems, account estates, or record sets are definitely in scope for the approved case?",
                "reason": "Strict mode prevents low-confidence asset suggestions from being treated as implementation-ready without clarification.",
                "affected_fields": ["candidate_assets"],
                "severity": "blocking",
            }
        )
    return questions


def _build_source_ref(text: str, excerpt: str, ref_id: str) -> dict[str, Any]:
    if excerpt and text:
        lowered_text = text.lower()
        lowered_excerpt = excerpt.lower()
        char_start = lowered_text.find(lowered_excerpt)
        if char_start < 0:
            char_start = 0
        char_end = min(len(text), char_start + len(excerpt))
    else:
        char_start = 0
        char_end = 0
    return {
        "ref_id": ref_id,
        "excerpt": excerpt,
        "char_start": char_start,
        "char_end": char_end,
    }


def _apply_duplicate_detection(draft: dict[str, Any], user, folder: Folder) -> None:
    accessible_ids = _get_visible_asset_ids(user)
    folder_assets = Asset.objects.filter(folder=folder)
    if accessible_ids is not None:
        folder_assets = folder_assets.filter(id__in=accessible_ids)
    folder_assets = list(folder_assets.select_related("folder"))

    duplicate_candidates: list[dict[str, Any]] = []
    for candidate in draft.get("candidate_assets", []):
        matches = _find_candidate_matches(candidate, folder_assets)
        if matches:
            duplicate_candidates.append(
                {
                    "temporary_id": candidate["temporary_id"],
                    "proposed_name": candidate["proposed_name"],
                    "matches": matches,
                    "recommended_human_action": "review_reuse_before_create",
                }
            )
    draft["duplicate_candidates"] = duplicate_candidates


def _get_visible_asset_ids(user):
    if not getattr(user, "is_authenticated", False):
        return None
    try:
        return RoleAssignment.get_accessible_object_ids(
            folder=Folder.get_root_folder(),
            user=user,
            object_type=Asset,
        )[0]
    except Exception:
        return None


def _find_candidate_matches(candidate: dict[str, Any], folder_assets: list[Asset]) -> list[dict[str, Any]]:
    proposed_name = str(candidate.get("proposed_name") or "").strip()
    proposed_ref = str(candidate.get("proposed_reference_id") or "").strip()
    normalized_name = _normalize_token(proposed_name)
    matches: list[dict[str, Any]] = []
    seen_ids: set[str] = set()

    for asset in folder_assets:
        match_type = None
        match_score = 0.0
        if proposed_name and asset.name == proposed_name:
            match_type = "exact_name_same_folder"
            match_score = 0.99
        elif proposed_name and _normalize_token(asset.name) == normalized_name:
            match_type = "normalized_name_same_folder"
            match_score = 0.9
        elif proposed_ref and asset.ref_id and asset.ref_id.strip().lower() == proposed_ref.lower():
            match_type = "reference_id_same_folder"
            match_score = 0.97

        if match_type and str(asset.id) not in seen_ids:
            seen_ids.add(str(asset.id))
            matches.append(
                {
                    "existing_asset_id": asset.id,
                    "existing_name": asset.name,
                    "existing_ref_id": asset.ref_id or None,
                    "existing_type": asset.get_type_display(),
                    "folder_id": asset.folder_id,
                    "folder_name": asset.folder.name,
                    "match_type": match_type,
                    "match_score": match_score,
                    "scope_relevance": "high",
                    "warning": "Potential duplicate within the selected folder.",
                }
            )
    return matches


def _normalize_token(value: str) -> str:
    return re.sub(r"[^a-z0-9]+", "", value.lower())


def _refresh_ambiguous_candidates(draft: dict[str, Any]) -> None:
    ambiguous_candidates = []
    for candidate in draft.get("candidate_assets", []):
        ambiguity_flags = candidate.get("ambiguity_flags") or []
        if ambiguity_flags:
            ambiguous_candidates.append(
                {
                    "temporary_id": candidate["temporary_id"],
                    "proposed_name": candidate["proposed_name"],
                    "ambiguity_flags": ambiguity_flags,
                    "recommended_actions": [
                        "accept_for_later_commit",
                        "mark_as_evidence_candidate",
                        "mark_as_control_candidate",
                        "mark_as_vulnerability_candidate",
                        "reject",
                        "defer",
                    ],
                }
            )
    draft["ambiguous_candidates"] = ambiguous_candidates


def _refresh_overall_confidence(draft: dict[str, Any]) -> None:
    candidate_assets = draft.get("candidate_assets") or []
    if candidate_assets:
        avg = sum(item.get("confidence", 0.0) for item in candidate_assets) / len(candidate_assets)
    else:
        avg = 0.0
    draft["overall_confidence"] = round(avg, 2)


def _ensure_low_confidence_blocking_question(
    draft: dict[str, Any],
    request_data: dict[str, Any],
) -> None:
    if not request_data.get("strict_mode"):
        return
    if draft.get("overall_confidence", 0.0) >= 0.75:
        return
    if draft.get("blocking_questions"):
        return
    draft.setdefault("blocking_questions", []).append(
        {
            "question_id": "low-confidence-asset-scope",
            "question_text": "Which concrete systems, account estates, or record sets are definitely in scope for the approved case?",
            "reason": "Strict mode requires human confirmation before low-confidence asset suggestions are treated as implementation-ready.",
            "affected_fields": ["candidate_assets"],
            "severity": "blocking",
        }
    )
