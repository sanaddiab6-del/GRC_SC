def _build_blocking_questions(
    *,
    organization_name: str | None,
    framework_name: str | None,
    strict_mode: bool,
) -> list[dict[str, Any]]:
    questions: list[dict[str, Any]] = []
    if organization_name:
        questions.append(
            {
                "question_id": "folder-reuse",
                "question_text": f"Should the draft reuse an existing Folder for {organization_name} or propose a new domain Folder?",
                "reason": "The parser cannot safely infer Folder reuse without a lookup and human confirmation.",
                "affected_fields": ["case_setup_draft.folder_domain_draft"],
                "severity": "blocking",
            }
        )
    if framework_name:
        questions.append(
            {
                "question_id": "framework-artifact",
                "question_text": f"Which exact {framework_name} library artifact should be used if multiple StoredLibrary entries match the request?",
                "reason": "The parser cannot resolve StoredLibrary, LoadedLibrary, or Framework ids without an explicit lookup.",
                "affected_fields": ["framework_resolution"],
                "severity": "blocking",
            }
        )
    if strict_mode and not framework_name:
        questions.append(
            {
                "question_id": "missing-framework",
                "question_text": "Which framework or library artifact should anchor this readiness case?",
                "reason": "Strict mode prevents the parser from guessing a framework when the scenario is ambiguous.",
                "affected_fields": ["preferred_framework", "framework_resolution"],
                "severity": "blocking",
            }
        )
    return questions


