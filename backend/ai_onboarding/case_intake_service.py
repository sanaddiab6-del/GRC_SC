def _normalize_request_data(request_data: dict[str, Any]) -> dict[str, Any]:
    normalized = dict(request_data)
    if normalized.get("known_deadline"):
        normalized["known_deadline"] = _iso_date(normalized["known_deadline"])
    normalized["assessment_period"] = _serialize_assessment_period(normalized.get("assessment_period"))
    normalized["user_locale"] = normalized.get("user_locale") or DEFAULT_USER_LOCALE
    normalized["strict_mode"] = bool(normalized.get("strict_mode", True))
    return normalized


def _annotate_provider_mode(
    draft: dict[str, Any],
    *,
    provider_mode: str,
    parser_note: str,
    warning: dict[str, Any] | None = None,
) -> None:
    source_summary = draft.setdefault("source_summary", {})
    parser_notes = source_summary.setdefault("parser_notes", [])
    if parser_note not in parser_notes:
        parser_notes.append(parser_note)
    source_summary["provider_mode"] = provider_mode
    if warning is not None:
        warnings = draft.setdefault("warnings", [])
        if not any(item.get("code") == warning["code"] for item in warnings if isinstance(item, dict)):
            warnings.append(warning)


def _normalize_provider_draft(
    provider_draft: dict[str, Any],
    normalized_request: dict[str, Any],
) -> dict[str, Any]:
    if not isinstance(provider_draft, dict):
        return build_case_intake_fallback_draft(normalized_request)

    serializer = AiCaseIntakeDraftSerializer(data=provider_draft)
    if serializer.is_valid():
        return provider_draft

    draft = build_case_intake_fallback_draft(normalized_request)
    overall_confidence = provider_draft.get("overall_confidence")
    if isinstance(overall_confidence, (int, float)):
        draft["overall_confidence"] = max(0.0, min(1.0, float(overall_confidence)))

    _annotate_provider_mode(
        draft,
        provider_mode="configured_local_provider",
        parser_note="provider_mode: configured_local_provider_shape_normalized",
        warning={
            "code": "configured_local_provider_shape_normalized",
            "message": "The local AI provider response was normalized to the Step 1 contract-safe draft shape.",
            "affected_fields": ["source_summary", "warnings"],
            "needs_review": True,
        },
    )
    return draft


def _extract_organization_name(scenario_text: str) -> str | None:
    match = re.match(r"^([A-Z][A-Za-z0-9&'\- ]{2,80}?)\s+is\b", scenario_text.strip())
    if match:
        return match.group(1).strip()
    return None


def _extract_framework_name(scenario_text: str) -> str | None:
    match = FRAMEWORK_RE.search(scenario_text)
    return match.group(0).strip() if match else None


def _extract_scope_hint(scenario_text: str) -> str | None:
    lowered = scenario_text.lower()
    if "payment platform" in lowered:
        return "payment platform operations"
    if "iam" in lowered:
        return "identity and access management scope"
    return None


def _extract_trigger(scenario_text: str) -> str | None:
    lowered = scenario_text.lower()
    if "launch" in lowered:
        return "pre-launch readiness review"
    if "regulatory" in lowered:
        return "regulatory readiness"
    return None


def _assessment_period_end(assessment_period: dict[str, Any] | None) -> str | None:
    if isinstance(assessment_period, dict):
        return _iso_date(assessment_period.get("end_date"))
    return None


def _build_business_objective(framework_name: str | None, scenario_text: str) -> str:
    if framework_name:
        return (
            f"Prepare a human-reviewed readiness draft for a {framework_name}-aligned assessment "
            "before any controlled object-creation step."
        )
    return "Prepare a human-reviewed readiness draft before any controlled object-creation step."


def _extract_geography(scenario_text: str) -> list[str]:
    lowered = scenario_text.lower()
    geographies: list[str] = []
    if "saudi" in lowered or "ksa" in lowered:
        geographies.append("Saudi Arabia")
    return geographies


