FRAMEWORK_RE = re.compile(r"\b[A-Z]{2,}(?:-\d(?::\d{4})?)\b")


class AiCaseIntakeService:
    def __init__(self, *, base_dir: str | None = None, provider=None):
        self.base_dir = base_dir or str(Path(__file__).resolve().parents[1])
        self.provider = provider

    def build_case_intake_draft(self, request_data: dict[str, Any]) -> dict[str, Any]:
        normalized = _normalize_request_data(request_data)
        provider = self.provider if self.provider is not None else get_case_intake_provider()

        if provider is None:
            draft = build_case_intake_fallback_draft(normalized)
            _annotate_provider_mode(
                draft,
                provider_mode="provider_not_configured_fallback",
                parser_note="provider_mode: provider_not_configured_fallback",
                warning={
                    "code": "provider_not_configured_fallback",
                    "message": "No AI provider is configured; the advisory draft was generated by the deterministic fallback parser.",
                    "affected_fields": ["source_summary", "warnings"],
                    "needs_review": True,
                },
            )
        else:
            try:
                provider_draft = provider.build_case_intake_draft(normalized)
                if isinstance(provider, AdvisoryCaseIntakeProvider):
                    draft = _normalize_provider_draft(provider_draft, normalized)
                else:
                    draft = provider_draft
                _annotate_provider_mode(
                    draft,
                    provider_mode="configured_local_provider",
                    parser_note="provider_mode: configured_local_provider",
                )
            except CaseIntakeProviderError as exc:
                if not exc.allow_fallback:
                    raise
                draft = build_case_intake_fallback_draft(normalized)
                _annotate_provider_mode(
                    draft,
                    provider_mode="local_provider_error_fallback",
                    parser_note="provider_mode: local_provider_error_fallback",
                    warning={
                        "code": "local_provider_error_fallback",
                        "message": exc.detail,
                        "affected_fields": ["source_summary", "warnings"],
                        "needs_review": True,
                    },
                )

        validate_case_intake_guardrails(draft, normalized, enforce_shape=False)

        serializer = AiCaseIntakeDraftSerializer(data=draft)
        if not serializer.is_valid():
            raise CaseIntakeDraftValidationError(
                "ai_draft_validation_failed",
                "Generated advisory draft did not pass contract validation.",
                serializer.errors,
            )

        validated_draft = serializer.data
        validate_case_intake_guardrails(validated_draft, normalized)
        return validated_draft


def build_case_intake_draft(
    request_data: dict[str, Any],
    *,
    base_dir: str | None = None,
    provider=None,
) -> dict[str, Any]:
    service = AiCaseIntakeService(base_dir=base_dir, provider=provider)
    return service.build_case_intake_draft(request_data)


