def _choose_perimeter_name(organization_name: str, scope_hint: str | None) -> str:
    if scope_hint:
        return f"{organization_name} - {scope_hint.title()}"
    return f"{organization_name} - GRC Readiness Scope"


def _choose_audit_name(organization_name: str, framework_name: str | None) -> str:
    if framework_name:
        return f"{organization_name} {framework_name} Readiness Review"
    return f"{organization_name} Readiness Review"


def _build_source_ref(scenario_text: str, excerpt: str, ref_id: str) -> dict[str, Any]:
    excerpt = excerpt.strip()
    char_start = scenario_text.find(excerpt) if excerpt else 0
    if char_start < 0:
        char_start = 0
        excerpt = scenario_text[: min(len(scenario_text), 160)]
    char_end = char_start + len(excerpt)
    return {
        "ref_id": ref_id,
        "excerpt": excerpt,
        "char_start": char_start,
        "char_end": char_end,
    }


def _serialize_assessment_period(assessment_period: Any) -> dict[str, Any] | None:
    if not isinstance(assessment_period, dict):
        return None
    serialized = dict(assessment_period)
    for key in ("start_date", "end_date"):
        serialized[key] = _iso_date(serialized.get(key))
    return serialized


def _iso_date(value: Any) -> str | None:
    if value is None or value == "":
        return None
    if isinstance(value, date):
        return value.isoformat()
    return str(value)